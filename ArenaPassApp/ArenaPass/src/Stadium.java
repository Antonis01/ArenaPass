public class Stadium {
    String stadiumName;
    int stadiumID;

    public Stadium(String stadiumName, int stadiumID) {
        this.stadiumName = stadiumName;
        this.stadiumID = stadiumID;
    }

    public String getStadiumName() {
        return stadiumName;
    }

    public int getStadiumID() {
        return stadiumID;
    }
}
